package vgu.vgu;

public class MultipleChoiceAnswer {
	private int id;
	private int questionId;
	private int studentId;
	private String answer;
	
	public int getId() {
		return id;
	}
	
	public int getQuestionId() {
		return questionId;
	}
	
	public int getStudentId() {
		return studentId;
	}
	
	public String getAnswer() {
		return answer;
	}
	
}
